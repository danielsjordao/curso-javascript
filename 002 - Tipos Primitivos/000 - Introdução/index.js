/*
    Um primitivo é um dado que não é representado através
    de um Objeto e, por consequência, não possui métodos.
    Na maior parte do tempo, um valor primitivo é representado
    diretamente através do mais baixo nível da implementação
    de uma linguagem.
    Tipos Primitivos:
     - String; Texto
     - Number; números
     - Boolean; verdadeiro ou falso
     - null; algo que veio nulo
     - undefined; algo que não foi definido no momento 
*/
